  const supported = ["ru", "en", "et"];
  const LS_KEY = "lang";

  const $ = (sel, root = document) => root.querySelector(sel);
  const $$ = (sel, root = document) => Array.from(root.querySelectorAll(sel));

  function detectLang() {
    const saved = localStorage.getItem(LS_KEY);
    if (saved && supported.includes(saved)) return saved;

    const langs = (navigator.languages && navigator.languages.length
      ? navigator.languages
      : [navigator.language]
    ).filter(Boolean);

    const first = (langs[0] || "en").toLowerCase();
    if (first.startsWith("ru")) return "ru";
    if (first.startsWith("et")) return "et";
    return "en";
  }

  function t(lang, key) {
    const base = i18n.en || {};
    const dict = i18n[lang] || base;
    return dict[key] ?? base[key] ?? "";
  }

  function applyLang(lang) {
    const label = $("#langLabel");
    if (label) label.textContent = lang.toUpperCase();

    document.documentElement.lang = lang;

    $$("[data-i18n]").forEach((el) => {
      const key = el.getAttribute("data-i18n");
      el.textContent = t(lang, key);
    });

    $$("[data-i18n-ph]").forEach((el) => {
      const key = el.getAttribute("data-i18n-ph");
      el.setAttribute("placeholder", t(lang, key));
    });

    // Update modal caption if open
    if ($("#modal")?.classList.contains("open")) {
      const capKey = $("#modal")?.getAttribute("data-caption-key");
      if (capKey) $("#modalCaption").textContent = t(lang, capKey);
    }

    localStorage.setItem(LS_KEY, lang);
  }

  function initLangMenu() {
    const wrap = $("#lang");
    const btn = $("#langBtn");
    const menu = $("#langMenu");

    if (!wrap || !btn || !menu) return;

    const open = () => {
      menu.classList.add("open");
      btn.setAttribute("aria-expanded", "true");
      menu.querySelector("button")?.focus();
    };

    const close = () => {
      menu.classList.remove("open");
      btn.setAttribute("aria-expanded", "false");
    };

    const toggle = () => (menu.classList.contains("open") ? close() : open());

    btn.addEventListener("click", toggle);

    menu.addEventListener("click", (e) => {
      const b = e.target.closest("button[data-lang]");
      if (!b) return;
      const lang = b.getAttribute("data-lang");
      applyLang(lang);
      close();
      btn.focus();
    });

    document.addEventListener("click", (e) => {
      if (!wrap.contains(e.target)) close();
    });

    document.addEventListener("keydown", (e) => {
      if (e.key === "Escape" && menu.classList.contains("open")) {
        close();
        btn.focus();
        return;
      }
      if (!menu.classList.contains("open")) return;

      if (e.key === "ArrowDown" || e.key === "ArrowUp") {
        e.preventDefault();
        const items = $$("#langMenu button");
        const active = document.activeElement;
        const idx = items.indexOf(active);
        const next = e.key === "ArrowDown" ? idx + 1 : idx - 1;
        items[(next + items.length) % items.length]?.focus();
      }
    });
  }

