  function initGallery(langGetter) {
    // Now we open per-project gallery from data-images
    const works = $$("[data-images]");
    const modal = $("#modal");
    const img = $("#modalImage");
    const cap = $("#modalCaption");
    const btnClose = $("#modalClose");
    const btnPrev = $("#modalPrev");
    const btnNext = $("#modalNext");

    if (!works.length || !modal || !img || !btnClose) return;

    let projectIndex = 0;
    let lastFocus = null;

    let projectImages = [];
    let imgIndex = 0;

    const getLang = () => langGetter();

    const renderImage = () => {
      if (!projectImages.length) return;
      img.src = projectImages[imgIndex];
    };

    const openAt = (i) => {
      projectIndex = (i + works.length) % works.length;
      const el = works[projectIndex];

      const capKey = el.getAttribute("data-caption-key");

      projectImages = (el.getAttribute("data-images") || "")
        .split(",")
        .map((s) => s.trim())
        .filter(Boolean);

      // fallback: if data-images is missing, use cover
      if (!projectImages.length) {
        const cover = $("img", el)?.getAttribute("src");
        projectImages = cover ? [cover] : [];
      }

      if (!projectImages.length) return;

      imgIndex = 0;
      lastFocus = document.activeElement;

      renderImage();

      modal.setAttribute("data-caption-key", capKey || "");
      cap.textContent = capKey ? t(getLang(), capKey) : "";

      modal.classList.add("open");
      modal.setAttribute("aria-hidden", "false");
      document.body.style.overflow = "hidden";
      btnClose.focus();
    };

    const close = () => {
      modal.classList.remove("open");
      modal.setAttribute("aria-hidden", "true");
      document.body.style.overflow = "";
      if (lastFocus && typeof lastFocus.focus === "function") lastFocus.focus();
    };

    const prev = () => {
      if (!projectImages.length) return;
      imgIndex = (imgIndex - 1 + projectImages.length) % projectImages.length;
      renderImage();
    };

    const next = () => {
      if (!projectImages.length) return;
      imgIndex = (imgIndex + 1) % projectImages.length;
      renderImage();
    };

    works.forEach((el, i) => {
      el.addEventListener("click", () => openAt(i));
      el.addEventListener("keydown", (e) => {
        if (e.key === "Enter" || e.key === " ") {
          e.preventDefault();
          openAt(i);
        }
      });
    });

    btnClose.addEventListener("click", close);
    btnPrev?.addEventListener("click", prev);
    btnNext?.addEventListener("click", next);

    modal.addEventListener("click", (e) => {
      if (e.target === modal) close();
    });

    document.addEventListener("keydown", (e) => {
      if (!modal.classList.contains("open")) return;

      if (e.key === "Escape") close();
      if (e.key === "ArrowLeft") prev();
      if (e.key === "ArrowRight") next();

      // focus trap
      if (e.key === "Tab") {
        const focusable = $$(".iconBtn, .modal__panel button", modal).filter(
          (el) => !el.disabled
        );
        if (!focusable.length) return;
        const first = focusable[0];
        const last = focusable[focusable.length - 1];
        if (e.shiftKey && document.activeElement === first) {
          e.preventDefault();
          last.focus();
        } else if (!e.shiftKey && document.activeElement === last) {
          e.preventDefault();
          first.focus();
        }
      }
    });
  }

  function initFormToast(langGetter) {
    const form = $("#contactForm");
    const toast = $("#toast");
    if (!form || !toast) return;

    let timer = null;

    const show = (msg) => {
      toast.textContent = msg;
      toast.classList.add("show");
      clearTimeout(timer);
      timer = setTimeout(() => toast.classList.remove("show"), 2600);
    };

    form.addEventListener("submit", (e) => {
      e.preventDefault();
      form.reset();
      show(t(langGetter(), "toastThanks"));
    });
  }

  function initSmoothScroll() {
    $$('a[href^="#"]').forEach((a) => {
      a.addEventListener("click", (e) => {
        const href = a.getAttribute("href");
        if (!href || href === "#") return;
        const target = document.querySelector(href);
        if (!target) return;
        e.preventDefault();
        target.scrollIntoView({ behavior: "smooth", block: "start" });
      });
    });
  }

  document.addEventListener("DOMContentLoaded", () => {
    let currentLang = detectLang();

    initLangMenu();
    applyLang(currentLang);

    const getLang = () => localStorage.getItem(LS_KEY) || currentLang;

    initMarqueeLoop(); // no-op
    initGallery(getLang);
    initFormToast(getLang);
    initSmoothScroll();

    // Update currentLang after selection
    $("#langMenu")?.addEventListener("click", (e) => {
      const b = e.target.closest("button[data-lang]");
      if (!b) return;
      currentLang = b.getAttribute("data-lang") || currentLang;
    });
  });
